package repls;

public class repl136 {
    private String m1="This is Private Method";

    String m2 = "This is Default Method";

    protected String m3 = "This is Protected Method";

    public String m4 = "This is Public Method";

    public static void main(String[] args) {
        repl136 obh=new repl136();
        System.out.println(obh.m1);
        System.out.println(obh.m2);
        System.out.println(obh.m3);
        System.out.println(obh.m4);

    }
}
